function extractString(id) {
    const startIdx = content.indexOf(`id: "${id}"`);
    const propStart = content.indexOf('readContent: "', startIdx) + 14;
    let endIdx = propStart;
    while (endIdx < content.length) {
        if (content[endIdx] === '"' && content[endIdx - 1] !== '\\\\') break;
        endIdx++;
    }
    return [propStart, endIdx, content.slice(propStart, endIdx)];
}

